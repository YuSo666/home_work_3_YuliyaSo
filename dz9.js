let time=prompt ('Сколько часов в день ты проводишь в соцсетях?')
if (time<=0) {
    alert ('Ты наверное кот');
} else if (time<=3){
    alert('Ты не зависим от соцсетей');
} else if (time<=6){
    alert('Тебе нужно сокращать время в соцсетях');
}else if (time>=7){
    alert('Если ты не зарабатываешь на этом, тебе нужно что-то менять в этой жизни');
}